                        立体放映早在电影发明之前就已出现。1868年，亨利·达尔梅达就曾用红绿两种光线把画面重复投射到银幕上，观众可以透过一种红绿双色的镜片把两个画面混合成黑白立体形象。这种方法日后被称为"双色立体放映法"(Anaglyphe)，曾在1935年前后被路易·卢米埃尔及美国的米高梅公司用来放映短片。 "双色立体放映法"的缺点是使人们不能看到彩色，所以(尤其在德国)有人用两片无色的偏镜片(Polarlides)来代替原先的红绿镜片，使光线从左边或右边偏离分散。 到1950年，这种方法开始走出实验室，在意大利、匈牙利和英国作公开的表演。 1952年11月在纽约，亚区·阿波勒放映了一部立体影片《魔鬼勃华那》，广告上用了这样引人注意的字样:"狮子爬上您的双膝"，"请看爱情的立体表演"。这部描写猛兽的探险片是用低成本在南非拍摄的，它获得如此大的成功，以致好莱坞各大公司竞相用立体电影来拍摄恐怖片(如《戴蜡面具的人》)，表现珍尼·鲁塞尔或丽塔·海华斯的色相的影片(如《法兰西路线》，《萨蒂·汤姆逊小姐》)。但是"偏光镜公司"出租它的眼镜收费之高，正象杀掉一只生金卵的母鸡，观众也对这种电影渐感厌烦，因为这种立体电影顶多只能表现演员在景深镜头中的一些活动，或者靠摄影机的移动来产生某些立体的效果。到1954年年底，好莱坞放弃了立体电影，同时在苏联，伊万诺夫发明的立体电影仍在一些大城市里放映，吸引有限的观众。 "星涅拉马"式宽银幕电影是和立体电影同时在纽约出现的，不过它的流行要持久得多。这种电影的发明者弗莱德·华勒(1954年故世)在放映时采用凹三面银幕和立体音响，就如阿贝尔·冈斯和机械师德布里于1927年和1935年在巴黎两次放映《拿破仑》时所用的方法那样。1940年华尔特·迪斯尼在美国几个城市里放映他的影片《幻想曲》 时也采用三条音带与三个扩音器，来产生立体声的效果。 "星涅拉马"式宽银幕电影用3架放映机，300平方米的凹三面银幕，6条声带和将近20个扩音器。它在纽约获得如此大的商业成功，以致观众要好几个月前预订影院的座位。 经过十年获利的经营，到1962年年底世界各地共有140家"星涅拉马"电影院，放映麦克·托德、茂里安·古柏、路易·德·罗希蒙等人拍摄的九十部影片。这些影片当时还不是故事片，而是一些旅游纪录片，引导观众漫游世界五大洲。当表现交通工具(火车、飞机、小汽车、直升飞机、游览小火车等)迎面飞驰而来时，尤其能产生惊人的效果。 这种立体效果更因哈扎尔·黎夫斯创造的立体声而大大加强。 1962年之后，凹三面银幕简化为一个大银幕，上面只放映70毫米的一部影片。"星涅拉马"式宽银幕电影开始用来放映故事片，但这些故事片在商业上的几次失败，使观看这种电影的人数明显下降。 在"星涅拉马"式宽银幕电影基础上发展起来的环幕电影是由华尔特·迪斯尼出资兴办的。这种电影要观众站在一个环形银幕的中央，银幕上面有11个孔，透过这些孔放映11部16毫米的影片，使周圈360度的银幕上都投射上画面。观众可以瞩目四望，可是很快就会被这种象巡回演出商的演出弄得疲惫不堪，放映很难继续半小时以上，因此至今还没有人为这种电影制作故事片。 "星涅拉马"式宽银幕电影积极倡导者之一的麦克·托德以其惊人的宣传才干大力推销一种名叫"托德-A.O."(A.O.是美国光学公司的简称)的宽银幕电影放映法。这种方法是用70毫米的影片在放映机里向横方面展开，把画面形象投射到几个巨大的银幕上，这些画面比标准影片的画面大十倍。在他不幸于1958年丧身于飞机失事之前，这位善于经营的制片人在用托德-A.O.法放映的两部故事片上发了一笔大财，这两部故事片一部是由弗莱德·齐纳曼导演的轻歌剧片《俄克拉荷马》，另一部故事片是《八十天环游世界》，尤其获得成功，这是一部天真幻想的大场面片，根据儒勒·凡尔纳原作的情节自由改编。到1958年年底止，全世界已有200家电影院安装了放映托德-A.O.影片的设备，另外，为了普及放映起见，托德-A.O.的影片还被转印到35毫米的标准胶片上面。同样也有人着手试验"深景电影"(Vistavision)，这种电影也是用一条35毫米的影片在放映机内向横的方向展开，影片的画格尺寸为35×35毫米，这种尺寸是那些使用莱卡摄影机的摄影师们所很熟悉的。

