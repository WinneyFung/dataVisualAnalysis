                        1908年以后，爱迪生和比沃格拉夫的电影托拉斯似乎已取得胜利。性情急躁的肯尼迪此时除了一些无足轻重的旧货商、马戏团丑角、假宝石商、鱼贩或兔皮以及一些无业游民所经营的电影业以外，已没有什么敌手。这些人几乎都是新来美国的移民，其中包括:威廉·福斯、卡尔·莱默尔、L.B.梅育、巴拉本、卡兹、凯赛尔、包曼、华纳兄弟、阿道尔夫·楚柯尔、塞缪尔·高德费许等人。当时肯尼迪所担心的是欧洲方面的竞赛。经营音乐咖啡馆的巨商茂笃克，曾把旧大陆上被百代和梅里爱所排挤的几个大制片公司联合起来，组成了一个"国际制片与放映公司"，自己担任这个公司的经理。但是，巴黎"受骗者会议"的失败，使茂笃克丧失电影史了勇气，他转而对史密斯-欧本的天然彩色胶片很感兴趣，而听任这个昙花一现的联合企业趋于瓦解。肯尼迪一方面极力促成欧洲方面竞争者的失败，另一方面，用威胁的方法将电影企业中的三个部门加速集中在自己手里。到1910年，他在这方面已经获得胜利。"电影专利公司"①的一个分支机构--通用影片公司，用200万美元把57家主要的发行公司收买过来。从此以后，美国9480家影院中，受这一托拉斯支配的有5281家，占全部影院的57%。①即被一般独立制片商称为"爱迪生托拉斯"的影片公司。如果竞争的对象是汽车工业或者是罐头工业，那么上一胜利可说已经是一个决定性的胜利。可是电影的技术日新月异，1908年认为很好的摄制方式，到1911年就成为不流行的方式，而爱迪生托拉斯却始终固步自封，使用老一套的方式。 凡是属于爱迪生托拉斯系统的每一大制片公司当时都有它自己的按年付酬的演员和导演。他们每星期要拍三四部影片。这些影片都不准超过一本，所花的费用也不得超过数百美元。商人出身的肯尼迪一定记得他从前经营的洋钉托拉斯虽享有专利权的独占，但由于这一行业，别人只要有一万美元的资本就可和他竞争的缘故，结果归于失败。而摄制一些一本长的影片，投资人只要雇用一些失业的演员，在露天或者在简陋的布景中扮演他们自己不会扮演的角色，所费成本比制造洋钉还更少。以赌马为业的凯赛尔和包曼只用了3000美元就创立了他们的影片公司。他们摄制的影片，每部成本不过200美元，而获得的收入却超过10倍。因此他们大量摄制影片，把这些影片推销给他们所控制的租片商。他们发迹的经过，和贩卖留声机的商人威廉·鲍威斯、戏剧商爱德温·丹霍塞、经营"镍币戏院"的卡尔·莱默尔、福斯、楚柯尔等人的发迹经过，实际上大致相同。这些颇为自负地自称为"独立的"新制片商，用增加几块钱津贴的方法，吸引托拉斯的技术人员到他们那里工作。威廉·雷努斯和保罗·潘采尔就是这样脱离了维太格拉夫公司，爱德温·鲍特也同样离开了爱迪生公司。这些以前做过巡回演出商的独立制片商比托拉斯的老板们更熟悉群众的爱好。潘采尔到鲍威斯那里以后，第一次就拍了一部《双枪将》，在六个月里就获得5万美元的纯利。 这些新成立的影片公司的生意日趋兴旺，因为托拉斯不能满足"镍币戏院"日益增长的需要，每星期最多只能供给两个到三个节目。有些地区的"镍币戏院"，虽然和托拉斯订有合同，但为了自己的利益，往往又和独立制片商发生联系，企图从他们那里获得一些新出的影片。肯尼迪利用报纸对"镍币戏院"这种背信行为大肆攻击，运动政府设立检查上映影片的机关，并勾结警察借口所谓安全条例，查封独立的影院。可是他这些措施对分散在象欧洲那样广大地区上的几千个放映商来说，却没有什么效果。于是他又唆使艾尔·麦考埃指挥下的流氓来攻击人数较少、易于击破的独立制片商。他们把硫酸倒在显影药水里，使底片全部毁掉，摄影机被砸烂，真的子弹从演牛仔的演员耳边掠过，在群众演员中间引起斗殴，造成伤亡事件。肯尼迪的这种办法，完全承袭了他的旧东家洛克菲勒的故技，后者曾经派遣武装匪徒，用炸药炸毁美孚公司的竞争者的输油管。 石油托拉斯可以用垄断输油管和炼油厂的办法来建立它的独占，可是这种办法在影片的流通和发行方面却很难想象。但伊斯特曼给予电影专利公司的胶卷独占权却有可能使它在打垮独立制片商方面取得胜利。然而由伯吕拉都尔经理的卢米埃尔工厂在美国的分厂，不久就出售了好几百万英尺的胶片，而里昂老厂的产量却没有任何增加。事实上，所有印有卢米埃尔兄弟公司商标的胶片都是在美国罗切斯特制造的。到了1911年，伊斯特曼放弃了这种诡计，公开宣布废弃和托拉斯签定的合同;而伯吕拉都尔本人则成了伊斯特曼的胶片销售总经理。但另一方面，在爱迪生这方面还有"山立格"、"维太格拉夫"、"卡勒姆"、"比沃格拉夫"等这些营业显然很兴旺的影片公司，继续和独立制片商进行斗争。以前经营地毯业的芝加哥人山立格"上校"，此时已成为规模庞大的摄影场、服装车间和样片洗印厂的老板了。他的影片公司专门摄制西部片。他的演员们走遍全美国，寻找风光明媚的地点。1908年初，摄影师托马斯